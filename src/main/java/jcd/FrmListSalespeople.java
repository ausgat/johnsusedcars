    /**
     * Fill the salesperson table (tblSalespersons) with all the Salesperson relations in the database
     */
    public void populateSalespeople() {

        // Use the SalespersonHandler to return a list of salespeople from the database as
        // Salesperson objects
        List<Salesperson> sps = sph.getSalespeople();

        // Magic DefaultTableModel code for working with Swing JTable objects
        // (no need to understand; just copy and paste)
        String[] columns = new String[] { "ID", "Username", "Name", "Phone", "Email" };
        DefaultTableModel tblModel = new DefaultTableModel(columns, 0) {
            // Make sure none of the cells is editable
            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        sps.forEach((sp)->{
            // For each row from the database, add it to the salesperson table
            tblModel.addRow(sp.getRow());
        });
        tblSalespeople.setModel(tblModel);
    }
    
