    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // Get the index of the selected row
        int selectedRow = tblSalespeople.getSelectedRow();

        // If a row is actually selected (that is, not -1)
        if (selectedRow != -1) {

            // Get the ID number from the first column (0) of the selected row
            int id = (int)tblSalespeople.getValueAt(selectedRow, 0);

            Salesperson sp = sph.findSalesperson(id);

            if (sp != null) {
                DlgUpdateSalesperson dlg = new DlgUpdateSalesperson(null, true);
                dlg.setSalesperson(sp);
                dlg.setVisible(true);
                if (dlg.getReturnStatus() == DlgUpdateSalesperson.RET_OK) {
                    this.populateSalespeople();
                }
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // Get the index of the selected row
        int selectedRow = tblSalespeople.getSelectedRow();

        // If a row is actually selected (that is, not -1)
        if (selectedRow != -1) {

            // Get the ID number from the first column (0) of the selected row
            int id = (int)tblSalespeople.getValueAt(selectedRow, 0);

            // Don't allow deletion if selected salesperson is logged in
            if (GlobalData.getSalesperson().getId() == id) {
                JOptionPane.showMessageDialog(this,
                        "Cannot delete logged-in salesperson.");
                return;
            }
            
            // Show a dialog and put the result in res
            int res = JOptionPane.showConfirmDialog(this,
                String.format("Are you sure you want to delete the salesperson with ID %d? This will delete all their sales.", id));

            // If the user clicked OK
            if (res == JOptionPane.OK_OPTION) {
                // Delete the selected salesperson
                sph.deleteSalesperson(id);

                // Reload the list
                populateSalespeople();
            }
        } else {
            JOptionPane.showMessageDialog(this,
                "Please select a salesperson to delete.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

